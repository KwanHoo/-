-- LV2
-- String, Date
-- DATETIME에서 DATE로 형 변환

-- MySQL
SELECT
    ANIMAL_ID,
    NAME,
    DATE_FORMAT(DATETIME, '%Y-%m-%d') AS '날짜'
FROM
    ANIMAL_INS
ORDER BY
    ANIMAL_ID

-- Oracle
SELECT
    ANIMAL_ID
    , NAME
    , TO_CHAR(DATETIME, 'YYYY-mm-dd') AS 날짜
FROM
    ANIMAL_INS
ORDER BY
    ANIMAL_ID